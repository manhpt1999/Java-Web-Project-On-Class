package model;

import java.io.Serializable;

/**
 *
 * @author Admin
 */
public class PizzaOrderHistory implements Serializable{
    private String image;
    private String name;
    private String price;
    private String size;

    public PizzaOrderHistory() {
    }


    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "PizzaOrderHistory{" + "image=" + image + ", name=" + name + ", price=" + price + ", size=" + size + '}';
    }
    
    
}
